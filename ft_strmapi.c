#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	size_t			str_l;
	unsigned int	i;

	i = 0;
	if (!s)
		return (NULL);
	str_l = ft_strlen(s);
	str = (char *)malloc(str_l + 1);
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
